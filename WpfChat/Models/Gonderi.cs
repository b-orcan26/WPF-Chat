using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace WpfChat.Models
{
    public class Gonderi
    {
        private String gonderen;
        private String ileti;

        public String Gonderen
        {
            get
            {
                return gonderen;
            }
            set
            {
                gonderen = value;
            }
        }

        public String Ileti
        {
            get
            {
                return ileti;
            }
            set
            {
                ileti = value;
            }
        }


    }
}
